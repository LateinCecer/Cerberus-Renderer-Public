    private final byte r, g, b;

    CFXColor(String hexColor) {
        Integer rawBits = bitsFromHexColor(hexColor);
        if (rawBits == null)
            rawBits = 0;

        this.r = (byte) ((rawBits >> 16) & 0xFF);
        this.g = (byte) ((rawBits >> 8) & 0xFF);
        this.b = (byte) (rawBits & 0xFF);
    }

    CFXColor(float red, float green, float blue) {
        this(new Vector3f(red, green, blue));
    }

    CFXColor(byte red, byte green, byte blue) {
        this.r = red;
        this.g = green;
        this.b = blue;
    }

    CFXColor(Vector3f diffuse) {
        r = (byte) (diffuse.getX() * 255f);
        g = (byte) (diffuse.getY() * 255f);
        b = (byte) (diffuse.getZ() * 255f);
    }

    public String getHexColor(Float alpha) {
        return toHexColor(r, g, b, alpha);
    }

    public String getHexColor() {
        return toHexColor(r, g, b, null);
    }

    public int rawBits(Float alpha) {
        return toRawBits(r, g, b, alpha);
    }

    public int rawBits() {
        return toRawBits(r, g, b, null);
    }

    public Vector3f getDiffuse() {
        return new Vector3f((float) Byte.toUnsignedInt(r) / 255f,
                (float) Byte.toUnsignedInt(g) / 255f,
                (float) Byte.toUnsignedInt(b) / 255f);
    }

    @Override
    public String toString() {
        return "\u001b[#" + getHexColor();
    }

    public static String toHexColor(Vector3f diffuse, Float alpha) {
        byte red = (byte) (diffuse.getX() * 255f);
        byte green = (byte) (diffuse.getY() * 255f);
        byte blue = (byte) (diffuse.getZ() * 255f);
        return toHexColor(red, green, blue, alpha);
    }

    public static String toHexColor(byte red, byte green, byte blue, Float alpha) {
        int rawBits;
        if (alpha == null) {
            rawBits = ((red & 0xFF) << 16) +
                    ((green & 0xFF) << 8) +
                    (blue & 0xFF);
        } else {
            rawBits = ((red & 0xFF) << 24) +
                    ((green & 0xFF) << 16) +
                    ((blue & 0xFF) << 8) +
                    (((byte) (alpha * 255f)) & 0xFF);
        }

        StringBuilder out = new StringBuilder(Integer.toHexString(rawBits));
        while (out.length() < 6)
            out.insert(0, "0");
        return out.toString();
    }

    public static int toRawBits(byte red, byte green, byte blue, Float alpha) {
        if (alpha == null) {
            return ((red & 0xFF) << 16) +
                    ((green & 0xFF) << 8) +
                    (blue & 0xFF);
        } else {
            return ((red & 0xFF) << 24) +
                    ((green & 0xFF) << 16) +
                    ((blue & 0xFF) << 8) +
                    (((byte) (alpha * 255f)) & 0xFF);
        }
    }

    public static int toRawBits(Vector3f diffuse, Float alpha) {
        byte red = (byte) (diffuse.getX() * 255f);
        byte green = (byte) (diffuse.getY() * 255f);
        byte blue = (byte) (diffuse.getZ() * 255f);
        return toRawBits(red, green, blue, alpha);
    }

    public static int toARGB(float alpha, float red, float green, float blue) {
        return (((byte) (alpha * 255f) & 0xFF) << 24) +
                (((byte) (red * 255f) & 0xFF) << 16) +
                (((byte) (green * 255f) & 0xFF) << 8) +
                ((byte) (blue * 255f) & 0xFF);
    }

    public static Integer bitsFromHexColor(String hexColor) {
        try {
            return Integer.parseInt(hexColor, 16);
        } catch (NumberFormatException e) {
            return null;
        }
    }

    public static Vector3f colorFromBits(Integer rawBits, boolean hasAlpha) {
        if (rawBits == null)
            return new Vector3f(0, 0, 0);

        if (hasAlpha) {
            float red = (float) ((rawBits >> 16) & 0xFF) / 255f;
            float green = (float) ((rawBits >> 8) & 0xFF) / 255f;
            float blue = (float) (rawBits & 0xFF) / 255f;
            return new Vector3f(red, green, blue);
        } else {
            float red = (float) ((rawBits >>> 24) & 0xFF) / 255f;
            float green = (float) ((rawBits >> 16) & 0xFF) / 255f;
            float blue = (float) ((rawBits >> 8) & 0xFF) / 255f;
            return new Vector3f(red, green, blue);
        }
    }
